    //inserisce un elemento vista all'interno del database
    private void insertVistaButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertVistaButton1ActionPerformed
        try{ 
            if(campiVuoti()){
                JOptionPane.showMessageDialog(this, "Prima di effettuare l'inserimento della vista\nriempire tutti i campi obbligatori !", "Attenzione", JOptionPane.WARNING_MESSAGE);
            }else{
                pstmt = Database.getDefaultConnection().prepareStatement(queryIns);            
            
                pstmt.setString(1, nomeVistaTextField.getText());
                pstmt.setString(2, queryVistaTextArea.getText());
                pstmt.setString(3, (String)proprVistaComboBox.getSelectedItem());
                pstmt.setString(4, (String)schemaVistaComboBox.getSelectedItem());
                righeInserite = pstmt.executeUpdate();
            
                if(righeInserite != 0){
                    JOptionPane.showMessageDialog(this, "La vista " + nomeVistaTextField.getText() + " Ã¨ stata inserita correttamente !", "Inserimento Vista effettuato", JOptionPane.INFORMATION_MESSAGE); 
                    pulisciInput();
                } 
                
                if(pstmt != null) pstmt.close();
            }
        }catch(SQLException e) {
            mostraErrore(e);
        }
    }//GEN-LAST:event_insertVistaButton1ActionPerformed


