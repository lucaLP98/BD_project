    private void oggComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oggComboBoxActionPerformed
        if(oggComboBox.getSelectedIndex() != -1){
            switch(oggComboBox.getSelectedItem().toString()){
                //crea un oggetto SearchTabella che permette la ricerca di una Tabella e lo aggiunge al searchPanel
                case "Tabella":
                    searchPanel.removeAll();
                    sT = new SearchTabella();
                    searchPanel.add(sT);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchDominio che permette la ricerca di un Dominio e lo aggiunge al searchPanel
                case "Dominio":
                    searchPanel.removeAll();
                    sD = new SearchDominio();
                    searchPanel.add(sD);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchTrigger che permette la ricerca di un Trigger e lo aggiunge al searchPanel
                case "Trigger":
                    searchPanel.removeAll();
                    sTr = new SearchTrigger();
                    searchPanel.add(sTr);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchProcedura che permette la ricerca di una Procedura / Funzione e lo aggiunge al searchPanel
                case "Procedura/Funzione":
                    searchPanel.removeAll();
                    sP = new SearchProcedura();
                    searchPanel.add(sP);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchViewSeqAssert che permette la ricerca di un' Asserzione e lo aggiunge al searchPanel
                case "Asserzione":
                    searchPanel.removeAll();
                    sVSA = new SearchViewSeqAssert(SearchViewSeqAssert.ASSERT);
                    searchPanel.add(sVSA);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchViewSeqAssert che permette la ricerca di una Vista e lo aggiunge al searchPanel
                case "Vista":
                    searchPanel.removeAll();
                    sVSA = new SearchViewSeqAssert(SearchViewSeqAssert.VISTA);
                    searchPanel.add(sVSA);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchViewSeqAssert che permette la ricerca di una Sequenza e lo aggiunge al searchPanel
                case "Sequenza":
                    searchPanel.removeAll();
                    sVSA = new SearchViewSeqAssert(SearchViewSeqAssert.SEQ);
                    searchPanel.add(sVSA);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
                
                //crea un oggetto SearchVincolo che permette la ricerca di un Vincolo e lo aggiunge al searchPanel
                case "Vincolo":
                    searchPanel.removeAll();
                    sV = new SearchVincolo();
                    searchPanel.add(sV);
                    searchPanel.revalidate();
                    searchPanel.repaint();
                break;
            } 
        }
    }//GEN-LAST:event_oggComboBoxActionPerformed

