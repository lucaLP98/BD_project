    //azzera i campi input del pannello
    public void pulisciInput(){
        nomeVistaTextField.setText("");
        proprVistaComboBox.setSelectedIndex(-1);
        schemaVistaComboBox.setSelectedIndex(-1);
        queryVistaTextArea.setText("");
    }
    
    
    //metodo che controlla se i campi di input sono stati riempiti o ne rimane almeno uno vuoto
    private boolean campiVuoti(){
        boolean tmp;
        
        tmp = nomeVistaTextField.getText().equals("") || proprVistaComboBox.getSelectedIndex() == -1 || schemaVistaComboBox.getSelectedIndex() == -1 || queryVistaTextArea.getText().equals("");
        
        return tmp;
    }
    
    //metodo utilizzato per riempire la proprVistaComboBox con gli username degli utenti che posseggono i permessi necessari alla creazione di una vista
    private void riempiProprVistaComboBox(){
        Statement stmt;
        ResultSet rst;
        String query = "SELECT U.username FROM utente U JOIN compRuoloSistema C ON U.ruolo = C.ID_ruolo WHERE C.ID_privSis = 'ALL PRIVILEGIES' OR C.ID_privSis = 'CREATE VIEW'";
        
        try{
            stmt = Database.getDefaultConnection().createStatement();
            rst = stmt.executeQuery(query);
            
            while(rst.next()){
                proprVistaComboBox.addItem(rst.getString(1));
            }
            
            proprVistaComboBox.setSelectedIndex(-1);
            
            stmt.close();
        }catch(SQLException e){
            mostraErrore(e);
        }
    }
    
    //metodo utilizzato per riempire la schemaVistaComboBox con gli schemi presenti nel database
    private void riempiSchemaVistaComboBox(){
        Statement stmt;
        ResultSet rst;
        String query = "SELECT nomeSchema FROM schema1";
        
        try{
            stmt = Database.getDefaultConnection().createStatement();
            rst = stmt.executeQuery(query);
            
            while(rst.next()){
                schemaVistaComboBox.addItem(rst.getString(1));
            }
            
            schemaVistaComboBox.setSelectedIndex(-1);
            
            stmt.close();
        }catch(SQLException e){
            mostraErrore(e);
        }
    }
    
    //metodo richiamato nella JPanel Insert utilizzato per riempire le JComboBox presenti in questo JPanel
    //in modo che contengano sempre dati aggiurnati  ai cambiamenti che avvengono nel DB
    public void riempiComboBox(){
        proprVistaComboBox.removeAllItems();
        schemaVistaComboBox.removeAllItems();
        this.riempiProprVistaComboBox();
        this.riempiSchemaVistaComboBox();
    }
    
    //Metodo che mostra a video l'errore generatosi durante l'inserimento di un elemento schema nel database
    private void mostraErrore(SQLException e) {
        String msg;
        
        msg = "Codice Errore: " + e.getErrorCode() + "\n";
        msg += "Messaggio: " + e.getMessage() + "\n";
        msg += "SQLState: " + e.getSQLState() + "\n";

        JOptionPane.showMessageDialog(this, msg, "Errore", JOptionPane.ERROR_MESSAGE);
    }

